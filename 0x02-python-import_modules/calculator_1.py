def add(a,b):
    """My addition function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        a + b
    """
    return a + b

def sub(a,b):
    """My Subtraction function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        a - b
    """
    return a - b

def mul(a,b):
    """My Multiplication function

    Args:
        a: first integer
        b: second integer

    Returns:
        a * b
    """
    return a * b

def div(a,b):
    """My Division function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        a / b
    """
    return a / b
